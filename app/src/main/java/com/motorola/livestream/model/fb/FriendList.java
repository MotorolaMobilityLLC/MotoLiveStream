package com.motorola.livestream.model.fb;

public class FriendList {
    private String id;
    private String name;

    public FriendList() { }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
